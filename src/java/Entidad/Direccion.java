
package Entidad;

/**
 *
 * @author Rodrigo
 */
public class Direccion {
    private String region;
    private String comuna;
    private String calle;
    private String nroCalle;
    private String nroCasa;
    private String rut;

    public Direccion() {
    }

    public Direccion(String region, String comuna, String calle, String nroCalle, String nroCasa, String rut) {
        this.region = region;
        this.comuna = comuna;
        this.calle = calle;
        this.nroCalle = nroCalle;
        this.nroCasa = nroCasa;
        this.rut = rut;
    }

    public String getRegion() {
        return region;
    }

    public void setRegion(String region) {
        this.region = region;
    }

    public String getComuna() {
        return comuna;
    }

    public void setComuna(String comuna) {
        this.comuna = comuna;
    }

    public String getCalle() {
        return calle;
    }

    public void setCalle(String calle) {
        this.calle = calle;
    }

    public String getNroCalle() {
        return nroCalle;
    }

    public void setNroCalle(String nroCalle) {
        this.nroCalle = nroCalle;
    }

    public String getNroCasa() {
        return nroCasa;
    }

    public void setNroCasa(String nroCasa) {
        this.nroCasa = nroCasa;
    }

    public String getRut() {
        return rut;
    }

    public void setRut(String rut) {
        this.rut = rut;
    }
    
    

    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
}
